<h2>The Lineup</h2>
<div class="speaker">
  <h3 id="paul">Paul English <span>Co-Founder, CTO, Kayak</span></h3>
  <img src="/img/homepage/paul_english.jpg" />
  <p>Paul English is Chief Technology Officer and cofounder. Before starting
  KAYAK, Paul was a lifelong technology entrepreneur. He co-founded Boston
  Light Software, a small business e-commerce platform that was sold to Intuit.
  At Intuit, Paul became Vice President of technology and led small business
  Internet and product strategy, company-wide innovation process, and tech
  recruiting.</p>
  <p>Paul was briefly an entrepreneur in-residence at Greylock, a co-founder and
  Director of Intermute, the founder of the World Xiangqi League, cofounder and
  Chairman of Mancala, Inc., Senior Vice President of Engineering and Product
  Management at Interleaf. He also did contract programming for the US Air
  Force, operations research programming for Data General, programming for a
  medical device company, and even some video game software and sound-effect
  development.</p>
  <p>Paul is involved in nearly every aspect of our company, but he spends most of
  his time leading product strategy and design, customer support and
  recruiting. Paul is obsessed with customer feedback. (He hates bad customer
  service so much that he launched the www.gethuman.com initiative to help
  consumers fight back against bad phone systems.)</p>
  <p>Paul received a BS and MS in computer science from University of
  Massachusetts. Paul regularly lectures on entrepreneurship topics at
  Boston-area universities, and was named Chief Technology Officer of the year
  by Mass Technology Leadership Council in 2009. He's also a Director at
  Partners-In-Health and Village Health Works, and is an advisor to JoinAfrica,
  FanSnap, Roost, LinkedIn, and Pixable.</p>
</div>
<div class="speaker">
  <h3 id="ari">Ari Gesher <span>Senior Engineer, Palantir</span></h3>
  <img src="/img/homepage/ari_gesher.jpg" />
  <p>Ari Gesher is a Senior Software Engineer at Palantir Technologies.
  Palantir Technologies is a thriving startup that is building the next
  generation of human-driven data analysis platforms and applications, now
  broadly deployed in a variety of applications in government, industry, and
  non-profits.</p>
  <p>An alumnus of the University of Illinois computer science department, Ari
  has worked in the software industry for the past ten years, including a stint
  as lead website and infrastructure maintainer for the SourceForge.net open
  source software archive.</p>
  <p>During his tenure at Palantir Technologies, Ari has split his time between
  working as a backend engineer on Palantir's analysis platform and thinking
  and writing about Palantir's vision for human-driven information data systems
  as well as devoting about half of his time working on Palantir’s Philanthropy
  Engineering team.</p>
</div>
<div class="speaker">
  <h3 id="yoky">Yoky Matsuoka <span>VP Technology, Nest</span></h3>
  <img src="/img/homepage/yoky_matsuoka.jpg" />
  <p>Yoky Matsuoka is VP of Technology at Nest Labs (www.nest.com). She received
  her Ph.D. at MIT in Electrical Engineering and Computer Science in the fields
  of Artificial Intelligence and Computational Neuroscience. She received an
  M.S. from MIT and a B.S. from UC Berkeley, both in EECS. She was also a
  Postdoctoral Fellow in the Brain and Cognitive Sciences at MIT and in
  Mechanical Engineering at Harvard University.</p>
  <p>She was Torode Family Endowed Career Development Professor of Computer
  Science and Engineering at the University of Washington, Director of NSF ERC
  Center for Sensorimotor Neural Engineering, and Ana Loomis McCandless
  Professor of Robotics and Mechanical Engineering at Carnegie Mellon
  University. Her work has been recognized with a MacArthur Fellowship,
  acclaimed as one of “The Brilliant Ten” in Popular Science Magazine, “Top 10
  Women to Watch in 2010” by Barbie, and "Power 25” in Seattle Magazine.</p>
</div>
<div class="speaker">
  <h3 id="john">John Bicket <span>Co-Founder, Meraki</span></h3>
  <img src="/img/homepage/john_bicket.png" />
  <p>John Bicket is responsible for the technical direction and development of
  Meraki. John holds a B.S. in Computer Science from Cornell University, and an
  S.M. degree in Computer Science from MIT. He was most recently a Ph.D.
  student in the Parallel and Distributed Operating Systems group at MIT, where
  he co-led the research project that won several academic awards and later
  became the foundation of Meraki’s wireless mesh technology. John, along with
  Sanjit Biswas and Hans Robertson, were able to turn the idea of Meraki into a
  reality.</p>
  <p>Meraki brings the benefits of the cloud to edge and branch networks,
  delivering easy-to-manage wireless, switching, and security solutions that
  enable customers to seize new business opportunities and reduce operational
  cost. Whether securing iPads in an enterprise or blanketing a campus with
  WiFi, Meraki networks simply work. In December 2012, Cisco acquired Meraki,
  forming Cisco’s new Cloud Networking Group. John continues to lead the
  technical team to continue to improve and expand Meraki while innovating new
  and exciting products.</p>
