					Kalma, a User Experience guru, jumpstarted his career at  Zappos.com as the Head of User  Experience, Creative Services and Marketing. He also led revolutionary projects that catalyzed the evolution of Zapposâ€™ user platform. He went on to serve as Senior Director of User Experience at Gilt Groupe and is currently Chief Experience Officer at Gemvara. Kalma recently founded Snipwits.com, a new interactive site for information sharing.    
