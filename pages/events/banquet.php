  <h1>Banquet</h1>
<p>The Techfair banquet is another opportunity for companies and students to connect. <a href="/companies/exhibitorlist/">Platinum, gold, and silver sponsors</a> will be able to reserve a table at the banquet to invite the students of their choice. Invites will be sent out by email at the conclusion of the fair to select students.</p>
<h2>Banquet Signup for Students</h2>
<p>In addition to invited students, Techfair is allowing students to signup for the banquet waitlist. Extra seats at the banquet will be given to students on a first-come-first-serve basis. <strong>Check back in February 2012</strong> for information on how to signup.</p>
