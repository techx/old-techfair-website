<h2>Student Invitations</h2>
<p>Company representatives will be able to invite students that they find via
the <a href="/drop">resume drop</a> as well as in person on the day of the
fair. Invited students will be notified by e-mail at the end of the fair with
instructions on how to accept the invitation.</p>
<p>All invitations must be accepted before 3pm the day of the fair.</p>
<h3>General Banquet Waitlist</h3>
<p>At Techfair, students will be able to place themselves on the general
banquet waitlist. Look for the waitlist at the Techfair exit booth. Extra seats
at the banquet will be given to waitlist students on a first-come, first-serve
basis.</p>
