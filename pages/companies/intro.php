<h2>Logistical Portal</h2>
<p>In the coming weeks you will receive your login information for the logistics portal, an online website. This will be the primary means of collecting and aggregating company data.</p>

<h2>Booths</h2>
<p>Company booths are the core of Techfair - they're a reflection of both the company and the fair. Students are looking for interesting and engaging booths with technology demos they can learn about and ideally try out.</p>

<p>We encourage every company to invest time into their Techfair display! We've seen incredible displays from a wide variety of companies. Organizations with tangible products have brought in prototypes of their latest models and designs, while software companies tend to demo their latest (and sometimes unreleased) products on large gorgeous displays. Others with products that are more difficult to demonstrate have instead compiled videos, infographics, and posters on the impact of their work and the science or engineering behind them. Just for fun, we've also seen baristas, popcorn machines, and swag for attendees.</p>

<p>If you have any ideas you want to run by us or any questions, don't hesitate to reach out.</p>

