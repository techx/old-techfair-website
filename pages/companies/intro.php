<h1>Welcome to Techfair!</h1>
<h2>About Techfair: what to expect</h2>
<p><em>Techfair 2013 is scheduled for Feb 4, 2012, the day before classes start, from 10AM-3PM (setup starts at 7AM).</em></p>

<p>Techfair is not your average career fair. Now in its fifth year, Techfair is a new take on how recruiting should look like at MIT. At this Institute, you'll find some of the most passionate and curious minds out there, and you'll find that appealing to their intellectual interests can be one of the most effective ways of persuading a student to join your cause.</p>

<p>Our mission is to bring technology to the recruiting process. We believe that, by bringing tech demos, excitement, and fun to the average career fair we can create an experience that benefits all parties involved.</p>

<p>We're extremely excited to have you and your organization on board - the fair is not too far away, and we have a lot of work to do together!</p>

