    <h2 id="banquet" class="new-info">Banquet</h2>
    <p>Platinum, gold, and silver sponsors get tables at the Techfair banquet. The
    banquet is an excellent opportunity for sponsors to connect with students of
    interest. <strong>You must indicate in the logistics portal that you are
    attending the banquet in order to have a table reserved for you.</strong></p>

    <p>The banquet is scheduled from <strong>6-8pm after the fair</strong>
    (February 4) at the <strong>Marriott in Kendall</strong> (Two Cambridge Center,
    50 Broadway Cambridge, Massachusetts, MA 02142). Attire is dressy casual.

    <h3>Banquet Invitation procedure</h3>
    <p>Each banquet table seats 10 people, and companies will be able to invite
    students to sit at their table. Note that the 10 people includes both company
    representatives and students, so if you have 3 representatives attending
    banquet only 7 students can attend. (Please fill out the food choice of the
    representatives in the portal.)</p>

    <p>Shortly after the resume drop opens (mid January), companies will be able to
    invite students through an online system.  More information on how to access
    the system will be released, alongside instructions on accessing the resume
    drop, when the resume drop opens. Students invited prior to the fair will be
    notified via email.</p>

    <p>Additionally, companies will be able to invite students throughout the day of
    the fair by collecting their MIT username and phone number. Students will be
    notified via text message as well as email. All invitations must be accepted by
    3pm the day of the fair.</p>

    <p>The first ten students to accept the invitation will be guaranteed spots at the
    banquet. Companies are allowed to extend as many invitations as they want, but
    any extra students will be placed on a waitlist in case of last minute
    cancellations.</p>

    <p>Finally, Techfair will maintain a general waitlist for students who are
    interested but have not yet received an invitation. We will do our best to
    match students to company tables for those tables with available seats and an
    empty waitlist.</p>

