<h1>About Us</h1>
<p>Techfair is completely <strong>student run</strong>, led by passionate and dedicated students at the Massachusetts Institute of Technology. Members of the board are split into eight committees: Corporate Relations, Marketing, Student Relations, Finance, Internal Relations, Information Technology, Logistics, and TechTalks.</p>
<p>To contact us, email <a href="mailto:techfair-exec@mit.edu">techfair-exec@mit.edu</a>.</p>
